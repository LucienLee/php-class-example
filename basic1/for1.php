<?php
	/*	使用 for 迴圈來做重複的事情。
		for ( 區塊1; 區塊2; 區塊3) {
			區塊4
		}
		區塊1是進入 for 時第一次做的動作，常稱呼它為初始化。只會執行一次。
		區塊2是一個判斷式，如同if的判斷式一樣，成立才會繼續執行，否則會離開for。
		區塊4是 for 主要執行的程式碼。
		區塊3是 區塊4 結束後會執行的程式碼。
		
		$i++ 的意思就是 $i=$i+1 也可以寫成 $i+=1
	*/
	for( $i=1 ; $i<=10 ; $i++ ) {
		echo "$i <br>";	
	}

?>